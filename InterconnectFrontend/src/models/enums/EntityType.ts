/**
 * Enumeration of entity types available in the simulation.
 */
enum EntityType {
    /** Represents a virtual machine entity */
    VirtualMachine = 1,
    /** Represents a network entity */
    Network = 2,
    /** Represents a virtual network node entity */
    VirtualNetworkNode = 3,
    /** Represents the Internet entity */
    Internet = 4,
}

export { EntityType };
